class Serie {
  final String nome;
  final int temporadaAtual;
  final int episodiosAssistidos;
  final int totalEpisodios;
  final String status;

  Serie({
    required this.nome,
    required this.temporadaAtual,
    required this.episodiosAssistidos,
    required this.totalEpisodios,
    required this.status,
  });
}